public class Assignment {
    public static void main(String[] args) {
        // simple
        int a = 10;
        System.out.println(a);

        // compound
        a += 10;
        System.out.println(a);

        a -= 10;
        System.out.println(a);

        a *= 10;
        System.out.println(a);

        a /= 10;
        System.out.println(a);

    }
}
