public class IncrementDecrement {
    public static void main(String[] args) {
        int a = 10;
        System.out.println(a);
        System.out.println("prepost Increment");
        System.out.println(a++);
        System.out.println(++a);

        System.out.println(" ");

        System.out.println(a);
        System.out.println("prepost Decrement");
        System.out.println(a--);
        System.out.println(--a);
    }
}
